package com.github.hburgmeier.javaone2013.samples.coffee;

public class CoffeeReadException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public CoffeeReadException() {
		super();
	}

	public CoffeeReadException(String message, Throwable cause) {
		super(message, cause);
	}

	public CoffeeReadException(String message) {
		super(message);
	}

	public CoffeeReadException(Throwable cause) {
		super(cause);
	}

}
